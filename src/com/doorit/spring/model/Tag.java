package com.doorit.spring.model;

public class Tag {

	public long id;
	public String tagName;
	
	
	public Tag() {
		
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getTagName() {
		return tagName;
	}
	public void setTagName(String tagName) {
		this.tagName = tagName;
	}
	
	

	
}